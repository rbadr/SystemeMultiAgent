package fr.univlille1.m2iagl.sma.environment;

public class Coordinate {

    private int x;
    private int y;

    public Coordinate(int x, int y) {
        super();
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}